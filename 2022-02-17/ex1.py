
"""
1) Escreva um algoritmo para ler as dimensões de um retângulo (base e
altura), calcular e escrever a área do retângulo.
"""

print("Montando um retângulo")
base=float(input("Digite o valor da base:"))
altura=float(input("Digite o valor da altura:"))
area=base*altura
print("A área desse retângulo é {:.1f} unidades".format(area))

